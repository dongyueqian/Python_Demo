'''
符号、对齐、用0填充

010 : 第1个0不是八进制
表示宽度为10，并且用0填充空位

'''
from math import pi
print('%10f' % pi) # 未填充
print('%010f' % pi) # 用0填充
print('%10.2f' % pi) # 未填充
print('%010.2f' % pi) # 用0填充

#  负号'-' :左对齐
print('%-10.2f' % pi)

print('**********************')

'''
正数前面加空格
+ 表示在转换之前加上正负号
'''
print(('% 5d' % 10) + '\n' + ('%+5d' % -10))
print('**********************')
print(('%+5d' % 10) + '\n' + ('%+5d' % -10))